#include <stdio.h>

int main()
{
    /* Declare variables to store numbers and a character */
    int a = 20;
    int b = 3;
    float c = 43.424;
    char d = 'C';
    /* Echo a, b, c and d */
    printf("The value of a is %d.\n", a);
    printf("The value of b is %d.\n", b);
    printf("The value of c is %f.\n", c);
    printf("The value of d is %c.\n", d);
    printf("\n");
    printf("Sesame Street was brought to you today by\n");
    printf("the letter %c and by the number %d.\n", d, b);
    return 0;
}
